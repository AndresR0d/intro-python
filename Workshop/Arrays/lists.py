"""
Arrays:
In Python, an "array" is not a built-in data type like it is in some other programming languages.
Instead, Python provides a similar data structure called a "list" that can be used
to store and manipulate collections of data. 

Characteristics:

Ordered: Lists maintain the order of the elements they contain.
You can access elements by their position (index) in the list.

Mutable: Lists are mutable, which means you can change their content by adding, removing,
or modifying elements.

Heterogeneous: Lists can store elements of different data types.
You can have a mix of integers, strings, floats, or even other lists within a single list.

Dynamic: Lists can grow or shrink in size as needed. You can add or remove elements dynamically.

Creation:
To create a list in Python, you enclose a sequence of elements within square brackets []
separated by commas. 

"""

# List example
my_list = [1, 2, 3, "Pikacku", 2.5]
print(my_list[0])

